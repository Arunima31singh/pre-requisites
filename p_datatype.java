public class p_datatype {
    public static void main(String[] args) {
        boolean bag = true;
        boolean notbag = false;
        byte B = 60;
        short S = 99;
        int num = 6789;
        long Lq = 999337L;
        float king = 99.698f;
        double d = 789.7890;
        char ch = 'a';
        String kk ="hello how are you";
     

        System.out.println("the boolean variable is " + bag );
        System.out.println("the boolean variable is " + notbag );
        System.out.println("the byte variable is " + B );
        System.out.println("the short variable is " + S );
        System.out.println("the int variable is " + num );
        System.out.println("the long variable is " + Lq );
        System.out.println("the float variable is " + king );
        System.out.println("the double variable is " + d );
        System.out.println("the char variable is " + ch );
        System.out.println(ch);
        System.out.println(kk);
    }
    
}
